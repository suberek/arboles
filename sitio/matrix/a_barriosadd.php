		if (!$this->barrio_nombre->FldIsDetailKey && !is_null($this->barrio_nombre->FormValue) && $this->barrio_nombre->FormValue == "") {
			ew_AddMessage($gsFormError, $Language->Phrase("EnterRequiredField") . " - " . $this->barrio_nombre->FldCaption());
